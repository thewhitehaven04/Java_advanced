package Exercise1_2.Calculator;


/**
 * This is the calculator class.
 */
public class Calculator {

    /**
     * Returns the sum of two floats.
     *
     * @param x Float class instance;
     * @param y Float class instance;
     * @return the sum of the floats.
     */
    public Float addFloat(Float x, Float y) {
        return x + y;
    }

    /**
     * Returns the difference between two floats.
     *
     * @param x Float class instance;
     * @param y Float class instance;
     * @return the difference between the two floats.
     */
    public Float subtractFloat(Float x, Float y) {
        return x - y;
    }

    /**
     * Returns the product of the floats.
     *
     * @param x Float class instance;
     * @param y Float class instance;
     * @return the product of the floats
     */
    public Float multiplyFloat(Float x, Float y) {
        return x * y;
    }

    /**
     * Returns the quotient of the floats.
     *
     * @param x Float class instance;
     * @param y Float class instance;
     * @return the quotient of the floats.
     */
    public Float divideFloats(Float x, Float y) {
        return x / y;
    }
}
